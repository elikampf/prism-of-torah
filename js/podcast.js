// Initialize podcast page functionality
document.addEventListener('DOMContentLoaded', function() {
  // Only run on podcast page
  if (!window.location.pathname.includes('podcast.html')) return;

  const accordion = document.getElementById('episode-accordion');

  // Populate archive from data
  function populateArchive() {
    const sefarim = Object.keys(window.podcastData);
    sefarim.forEach(sefer => {
      const seferSection = accordion.querySelector(`.sefer-section[data-sefer="${sefer}"]`);
      if (!seferSection) return;
      
      const seferContent = seferSection.querySelector('.sefer-content');
      // Remove loading placeholder
      seferContent.innerHTML = '';
      
      const parshiot = Object.keys(window.podcastData[sefer]);
      parshiot.forEach(parsha => {
        const episodes = window.podcastData[sefer][parsha];
        const parshaId = `${sefer.toLowerCase()}-${parsha.toLowerCase().replace(/\s+/g, '-')}`;
        
        // Parsha section
        const parshaSection = document.createElement('div');
        parshaSection.className = 'parsha-section';
        parshaSection.innerHTML = `
          <button class="parsha-header" aria-expanded="false" data-target="${parshaId}">
            <span class="parsha-icon">ðŸ“œ</span>
            <span class="parsha-title">${parsha}</span>
            <span class="parsha-count">(${episodes.length} episode${episodes.length !== 1 ? 's' : ''})</span>
            <span class="expand-icon">+</span>
          </button>
          <div class="parsha-episodes" id="${parshaId}" style="display:none;">
            ${episodes.map(ep => {
              const safeTitle = ep.title.replace(/'/g, "&#39;").replace(/"/g, '&quot;');
              return `
                <div class="episode-item">
                  <div class="episode-info">
                    <h4 class="episode-title">${ep.title}</h4>
                    ${ep.description ? `<p class="episode-description">${ep.description}</p>` : ''}
                  </div>
                  <a href="${ep.link}" target="_blank" class="btn-episode">Listen Now</a>
                </div>
              `;
            }).join('')}
          </div>
        `;
        seferContent.appendChild(parshaSection);
      });
    });
  }

  // Handle accordion clicks
  function handleAccordionClicks() {
    accordion.addEventListener('click', function(e) {
      // Sefer accordion
      const seferBtn = e.target.closest('.sefer-header');
      if (seferBtn) {
        const contentId = seferBtn.getAttribute('data-target');
        const content = document.getElementById(contentId);
        const expanded = seferBtn.getAttribute('aria-expanded') === 'true';
        
        // Collapse all sefer sections
        accordion.querySelectorAll('.sefer-header').forEach(btn => {
          btn.setAttribute('aria-expanded', 'false');
          const icon = btn.querySelector('.expand-icon');
          if (icon) icon.textContent = '+';
        });
        accordion.querySelectorAll('.sefer-content').forEach(div => div.style.display = 'none');
        
        if (!expanded) {
          seferBtn.setAttribute('aria-expanded', 'true');
          content.style.display = 'block';
          const icon = seferBtn.querySelector('.expand-icon');
          if (icon) icon.textContent = '-';
        }
        return;
      }
      
      // Parsha accordion
      const parshaBtn = e.target.closest('.parsha-header');
      if (parshaBtn) {
        const contentId = parshaBtn.getAttribute('data-target');
        const content = document.getElementById(contentId);
        const expanded = parshaBtn.getAttribute('aria-expanded') === 'true';
        
        // Collapse sibling parsha sections
        const parshaSections = parshaBtn.closest('.sefer-content').querySelectorAll('.parsha-header');
        parshaSections.forEach(btn => {
          btn.setAttribute('aria-expanded', 'false');
          const icon = btn.querySelector('.expand-icon');
          if (icon) icon.textContent = '+';
        });
        const parshaContents = parshaBtn.closest('.sefer-content').querySelectorAll('.parsha-episodes');
        parshaContents.forEach(div => div.style.display = 'none');
        
        if (!expanded) {
          parshaBtn.setAttribute('aria-expanded', 'true');
          content.style.display = 'block';
          const icon = parshaBtn.querySelector('.expand-icon');
          if (icon) icon.textContent = '-';
        }
        return;
      }
    });
  }

  // Initialize everything
  populateArchive();
  handleAccordionClicks();
});